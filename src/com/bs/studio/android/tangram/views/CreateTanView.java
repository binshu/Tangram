package com.bs.studio.android.tangram.views;


import android.content.Context;
import android.graphics.Canvas;
import android.util.AttributeSet;

/**
 * @author LemIST
 * 
 */
public class CreateTanView extends GameView {

	/**
	 * @param context
	 */
	public CreateTanView(Context context) {
		super(context);
	}

	/**
	 * @param context
	 * @param attrs
	 */
	public CreateTanView(Context context, AttributeSet attrs) {
		super(context, attrs);
	}

	@Override
	protected void onDraw(Canvas canvas) {
		super.onDraw(canvas);
		
		drawPieces(canvas);
	}

	@Override
	protected boolean hasTangram() {
		return false;
	}

}
