package com.bs.studio.android.tangram.model;

public enum PieceId {
	LargeTriangle1(0),
	LargeTriangle2(1),
	MiddleTriangle(2),
	SmallTriangle1(3),
	SmallTriangle2(4),
	Square(5),
	Parallelogram(6);
	
	PieceId(int value){
		this.value = value;
	}
	
	public int getValue (){
		return value;
	}
	
	private int value;
}
